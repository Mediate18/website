				<p>Juliette Reboul is trained as a historian. She specialises in the circulation of objects, ideas and people in eighteenth-century Europe. She worked on the <a href="http://fbtee.uws.edu.au/main/">FBTEE</a> project (University of Western Sydney), before joining  the MEDIATE team at Radboud University in Nijmegen. In this project, she focuses on printed auction catalogues of British private libraries. Her PhD thesis examined the experiences and memories of French emigrants in the British Isles in response to the French Revolution. It has been published in Palgrave Macmillan's  series  'War, Culture and Society, 1750-1850' as <a href="https://www.palgrave.com/la/book/9783319579955">French Emigration to Great Britain in Response to the French Revolution</a>. </p>
