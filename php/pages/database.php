	<h1 class="visible-xs">Database</h1>

	<p>
		Although the MEDIATE project is formally over, work continues harvesting, transcribing and enriching data from our corpus of (sales) catalogues of eighteenth-century private libraries. The <a href="https://mediate-database.cls.ru.nl/about/">MEDIATE database</a> (Measuring Enlightenment: Disseminating Ideas, Authors, and Texts in Europe, 1665 - 1830), contains fully searchable transcriptions and metadata on books and collectors extracted from a Sandbox corpus of 600 smaller private library (sales) catalogues. It is our aim to incrementally expand this initial Sandbox corpus until 2028. To that end, we have sourced an additional 200 catalogues, including Hebrew-language Dutch catalogues and catalogues from Spain, that we are in the process of turning into searchable data. Work is also ongoing in a second database: the <a href="https://heurist.huma-num.fr/heurist/?db=radboud_BIBLIO&website&id=890&pageid=884">BIBLIO database</a> (Bibliography of Individually-owned Book and Library Inventories Online, 1665 - 1830), containing basic bibliographic records for all extant private library (sales) catalogues in the Dutch Republic, France, and the British Isles, for the period 1665 – 1830.
	</p>

